public class _00_02_DataType {
    public static void main(String[] args) {
        byte b;                 // 1 byte
        short s;                // 2 bytes
        int i = 1;              // 4 bytes
        long l = 1234L;         // 8 bytes
        float f;                // 8 digits
        double d = 12.34;       // 16 digits
        boolean bl;             // true / false
        char c = 'a';
        String s = "OK";
        int k = 'z' - 'a';
    }
}